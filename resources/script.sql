CREATE TABLE boards
(
  id_board INT(5) PRIMARY KEY NOT NULL AUTO_INCREMENT,
  type CHAR(5) NOT NULL,
  cabinet INT DEFAULT 0,
  subrack INT DEFAULT 0,
  slot INT,
  full_type CHAR(15),
  serial_number CHAR(30)
);
ALTER TABLE boards ADD bs_name CHAR(10) NOT NULL;
CREATE TABLE base_station
(
  id_base INT(5) PRIMARY KEY NOT NULL AUTO_INCREMENT,
  name CHAR(10) NOT NULL,
  egbts BOOL DEFAULT TRUE  NOT NULL,
  soft_version INT(30)
);
ALTER TABLE base_station MODIFY soft_version CHAR(30);
